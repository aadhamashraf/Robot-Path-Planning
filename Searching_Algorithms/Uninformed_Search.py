def bfs(maze, start, goal):
    startTime = time.time()
    visited = [[False] * MAZE_WIDTH for _ in range(MAZE_HEIGHT)]
    queue = deque([start])
    parent = {start: None}

    visited[start[1]][start[0]] = True

    while queue:
        x, y = queue.popleft()
        print(f"Exploring BFS: ({x}, {y})")

        if (x, y) == goal:
            path = []
            while parent[(x, y)] is not None:
                path.append((x, y))
                x, y = parent[(x, y)]
            path.append(start)
            endTime = time.time()
            return path[::-1], endTime-startTime

        for dx, dy in DIRECTIONS:
            nx, ny = x + dx, y + dy
            if 0 <= nx < MAZE_WIDTH and 0 <= ny < MAZE_HEIGHT and not visited[ny][nx] and maze[ny][nx] == 0:
                visited[ny][nx] = True
                queue.append((nx, ny))
                parent[(nx, ny)] = (x, y)

    return None

# DFS Algorithm


def dfs(maze, start, goal):
    startTime = time.time()
    stack = [start]
    parent = {start: None}
    visited = set()
    steps = 0

    while stack:
        x, y = stack.pop()
        visited.add((x, y))
        steps += 1

        if (x, y) == goal:
            path = []
            while parent[(x, y)] is not None:
                path.append((x, y))
                x, y = parent[(x, y)]
            path.append(start)
            endTime = time.time()
            return path[::-1], steps, endTime-startTime

        for dx, dy in DIRECTIONS:
            nx, ny = x + dx, y + dy
            if 0 <= nx < MAZE_WIDTH and 0 <= ny < MAZE_HEIGHT and (nx, ny) not in visited and maze[ny][nx] == 0:
                stack.append((nx, ny))
                parent[(nx, ny)] = (x, y)

    return None

# IDS Algorithm


def ids(maze, start, goal, depth_limit):
    startTime = time.time()
    for depth in range(depth_limit):
        result = dls(maze, start, goal, depth)
        if result is not None:
            endTime = time.time()
            return result, endTime-startTime
    return None


def dls(maze, start, goal, depth):
    stack = [(start, 0)]
    parent = {start: None}
    visited = set()

    while stack:
        (x, y), current_depth = stack.pop()
        visited.add((x, y))

        if (x, y) == goal:
            path = []
            while parent[(x, y)] is not None:
                path.append((x, y))
                x, y = parent[(x, y)]
            path.append(start)
            return path[::-1]

        if current_depth < depth:
            for dx, dy in DIRECTIONS:
                nx, ny = x + dx, y + dy
                if 0 <= nx < MAZE_WIDTH and 0 <= ny < MAZE_HEIGHT and (nx, ny) not in visited and maze[ny][nx] == 0:
                    stack.append(((nx, ny), current_depth + 1))
                    parent[(nx, ny)] = (x, y)

    return None
